def counting_sort(arr, k):
    size = len(arr)
    output = [0 for i in range(0, size)]
    counter = [0 for i in range(0, k + 1)]
    print(arr)
    print(counter)
    print(output)
    print()
    print("Loop 1")
    for i in range(0, size):
        counter[arr[i]] += 1
        print(arr)
        print(counter)
        print(output)
        print()
    print("Loop 2")
    for i in range(1, k + 1):
        counter[i] += counter[i - 1]
        print(arr)
        print(counter)
        print(output)
        print()
    print("Loop 3")
    for i in range(size - 1, -1, -1):
        counter[arr[i]] -= 1
        output[counter[arr[i]]] = arr[i]
        print(arr)
        print(counter)
        print(output)
        print()
    return output

a = [0, 2, 3, 1, 0, 1, 2, 0, 0, 2]

counting_sort(a, 3)

