def jump_insertsort(A, start, step):
    for i in range(start + step, len(A), step):
        value = A[i]
        j = i
        while j >= step and A[j - step] > value:
            A[j] = A[j - step]
            j = j - step
        A[j] = value
        print(A, i, j, step, j - step, value)


def shellSort(A):
    print(A)
    seg_size = len(A) // 2
    while seg_size > 0:
        for start_i in range(seg_size):
            print(seg_size, start_i)
            jump_insertsort(A, start_i, seg_size)
            print(A)
        seg_size = seg_size // 2


a = [2, 0, 3, 0, 1, 1, 2, 3, 1]
shellSort(a)

